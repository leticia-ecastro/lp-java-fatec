public class OperadoresAritmeticos {
	
	public static void main (String[] args) {
		int a = 10 + 1;
		int b = 10 - 2;
		int c = 20 * 3;
		int d = 25 / 5;
		int e = 10 % 4;
		System.out.println(a); //Saída: 11
		System.out.println(b); //Saída: 8
		System.out.println(c); //Saída: 60
		System.out.println(d); //Saída: 5
		System.out.println(e); //Saída: 2
	}
}

